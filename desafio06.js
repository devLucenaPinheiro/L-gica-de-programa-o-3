let n = prompt("Digite um número de 0 à 6")

switch(n){
    case "0":
        alert("Domingo")
        break
    case "1":
        alert("Segunda-feira")
        break
    case "2":
        alert("Terça-feira")
        break
    case "3":
        alert("Quarta-feira")
        break
    case "4":
        alert("Quinta-feira")
        break
    case "5":
        alert("Sexta-feira")
        break
    case "6":
        alert("Sábado")
        break
    default:
        alert("Digite apenas números de 0 a 6")
}